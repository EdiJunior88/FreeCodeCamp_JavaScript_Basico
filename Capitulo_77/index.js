function switchOfStuff(val) {
  let answer = "";
  // Altere apenas o código abaixo desta linha
  switch (val) {
    case "a":
      return "apple";
      break;
    case "b":
      return "bird";
      break;
    case "c":
      return "cat";
      break
    default:
      return "stuff";
  }

  // Altere apenas o código acima desta linha
  return answer;
}

switchOfStuff(1);